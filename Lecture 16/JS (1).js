// Запрашиваем первое число у пользователя и преобразуем в число
const num1 = +prompt("Введите первое число:");

// Запрашиваем второе число у пользователя и преобразуем в число
const num2 = +prompt("Введите второе число:");

// Вычисляем сумму
const sum = num1 + num2;

// Выводим результат с помощью alert
alert(`Сумма чисел: ${sum}`);