// Получаем скорость и преобразуем в число
const speed = +prompt("С какой скоростью едет мотоциклист? (км/ч)");

// Получаем расстояние и преобразуем в число
const distance = +prompt("Какое расстояние должен преодолеть мотоциклист? (км)");

// Вычисляем время в часах
const time = distance / speed;

// Выводим результат с округлением до двух знаков после запятой
alert(`Мотоциклист приедет через ${time.toFixed(2)} часов`);